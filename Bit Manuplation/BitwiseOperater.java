public class BitwiseOperater{
    public static void main(String[] args) {
        int a=5,b=6;
        System.out.println(a&b);
        System.out.println(a|b);
        System.out.println(a ^ b); 
        System.out.println(~0);
        System.out.println(5<<2);
        System.out.println(6>>1);
    }
}